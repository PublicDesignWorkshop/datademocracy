                <h3><span class="glyphicon glyphicon-map-marker"></span> what we do</h3>
                <p>Technological development has opened the door to <strong>truly direct</strong> democratic participation. Online social media and the prevalence of user-generated content have provided an opportunity for everyone to <strong>have their say</strong>.</p>

                <p>Using a combination of <strong>data-mining</strong>, <strong>web-spidering</strong>, and <strong>sentiment analysis</strong>, Data Democracy provides realtime feelings about issues, supplying the <strong>grist of governance</strong>.</p>

                <hr />
                <h3><span class="glyphicon glyphicon-ok"></span> why it works</h3>
                <p>Real-time geolocated sentiment analysis provides a way for civic issues to be evaluated in real time, creating a governmental infrastructure that is <strong>responsive</strong>, <strong>responsible</strong>, and <strong>respectful</strong> of the needs of citizens.</p>

                <p>Because Data Democracy examines the feelings of political areas at all times, voting and its <strong>low turnout can be eliminated</strong>. Political parties and the rancor around elections, as well as <strong>wasteful campaign spending become unnecessary</strong>.</p>

                <hr />
                <h3><span class="glyphicon glyphicon-stats"></span> what you get</h3>
                <p>Data Democracy uses <strong>proven technologies</strong> that have been successfully applied to fields like <strong>advertising</strong> and <strong>marketing</strong>.</p>

                <p><strong>Understanding consumer habits</strong> is very similar to <strong>uncovering electorate sentiments</strong> around proposed civic and social issues. Data democracy works at the level of metadata, ensuring that <strong>no individually-identifiable information</strong> is kept from any single issue.</p>
